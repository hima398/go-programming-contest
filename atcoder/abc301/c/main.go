func solve01(s, t string) bool {
	ms, mt := make(map[byte]int), make(map[byte]int)
	for i := range s {
		ms[s[i]]++
	}
	for i := range t {
		mt[t[i]]++
	}
	for k, v := range ms {
		if k == '@' {
			continue
		}
		ms[k] -= Min(v, mt[k])
		mt[k] -= Min(v, mt[k])
		if ms[k] == 0 {
			delete(ms, k)
		}
		if mt[k] == 0 {
			delete(mt, k)
		}
	}
	//fmt.Println(ms)
	//fmt.Println(ms)
	atcoder := "atcoder"
	var containsAt bool
	for k := range ms {
		containsAt = containsAt || k == '@'
	}
	for k := range mt {
		containsAt = containsAt || k == '@'
	}
	//fmt.Println(containsAt)
	//ソートして考える
	if containsAt {
		for k := range ms {
			if k == '@' {
				continue
			}
			if !strings.Contains(atcoder, string(k)) {
				return false
			}
		}
		for k := range mt {
			if k == '@' {
				continue
			}
			if !strings.Contains(atcoder, string(k)) {
				return false
			}
		}
	} else {
		for k := range ms {
			if ms[k] != mt[k] {
				return false
			}
		}
	}
	return true
}

